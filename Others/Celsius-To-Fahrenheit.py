# Convert Celsius to Fahrenheit

# 1) Prompt the user to enter a temperature in Celsius.
# 2) Use the formula: F = (C * 9/5) + 32 to convert the temperature.
# 3) Print the converted temperature in Fahrenheit.

Temp_C = eval(input("Enter the Temperature in Celsius: "))
Temp_F = (Temp_C * 9/5) + 32
print('Temperature in Fehrenheit: ' + str(Temp_F))
